public class Pessoa {
    protected String nome, genero, cpf;

    public String getNome(){
        return this.nome;
    }
    public void setNome(String nome){
        this.nome=nome;
    }
    public String getGenero(){
        return this.genero;
    }
    public void setGenero(String genero){
        this.genero=genero;
    }
    public String getCpf(){
        return this.cpf;
    }
    public void setCpf(String cpf){
        this.cpf=cpf;
    }
}
